// Use an if statement
if x > 0 {
    fmt.Println("x is positive")
}

// Use an if-else statement
if x > 0 {
    fmt.Println("x is positive")
} else {
    fmt.Println("x is non-positive")
}

// Use an if-else if-else statement
if x > 0 {
    fmt.Println("x is positive")
} else if x < 0 {
    fmt.Println("x is negative")
} else {
    fmt.Println("x is zero")
}